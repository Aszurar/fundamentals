package number_and_strings

 fun main() {

//Numbers
     val firstNumber: Byte = -128 //-128 => 127 values
     val secondNumber: Short = -32_768 // -32_768 => -32_767
     val thirdNumber: Int = 1_000_000_000 // 32 bits
     val fourthNumber: Long = 1234561243561243 // 64 bits


//Strings
     val character: Char = 'a'
     val string: String = "Lucas de Lima"

     println(firstNumber)
     println(secondNumber)
     println(thirdNumber)
     println(fourthNumber)
     println(character)
     println(string)

 }
